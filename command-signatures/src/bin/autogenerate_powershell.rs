use core::panic;
use std::{fs, io, path, process};

use itertools::Itertools;
use rayon::prelude::*;

use warp_command_signatures::{
    fig_types::Command, overrides::CommandOverrides, powershell_autogenerator::CmdletHelp,
};

fn main() {
    println!("Getting all Cmdlet names...");
    let all_cmdlet_names =
        run_pwsh_command("Get-Command -Type Cmdlet | Select-Object -ExpandProperty Name");
    let all_cmdlet_names = all_cmdlet_names.trim().split('\n').collect_vec();

    println!("Parsing help pages for all Cmdlets...");
    let all_cmdlet_specs = all_cmdlet_names
        .par_iter()
        .map(|cmdlet_name| {
            let cmdlet_help_json =
                run_pwsh_command(format!("Get-Help {cmdlet_name} | ConvertTo-Json -Depth 8"));
            let mut cmdlet_help = serde_json::from_str::<CmdletHelp>(&cmdlet_help_json)
                .unwrap_or_else(|_| panic!("failed to deserialize {cmdlet_name} help"));
            let aliases = run_pwsh_command(format!(
                "Get-Alias -Definition {cmdlet_name} | Select-Object -ExpandProperty Name"
            ));
            cmdlet_help.aliases = aliases
                .trim()
                .split('\n')
                .filter(|val| !val.is_empty())
                .map(ToOwned::to_owned)
                .collect_vec();
            let mut fig_command: Command = cmdlet_help.into();

            if let Some(overrides) = get_overrides(cmdlet_name) {
                apply_overrides(&mut fig_command, overrides)
                    .expect(&format!("unable to apply arg override for {}", cmdlet_name));
            }

            fig_command
        })
        .collect::<Vec<Command>>();

    println!("Serializing and exporting command signatures...");
    all_cmdlet_specs.par_iter().for_each(|cmdlet_spec| {
        let name = cmdlet_spec.name.first().expect("Cmdlet must have a name");
        let file_path = path::Path::new(env!("CARGO_MANIFEST_DIR"))
            .join("json")
            .join("autogenerated")
            .join("powershell")
            .join(format!("{name}.json"));
        let mut json = serde_json::to_string_pretty(&cmdlet_spec)
            .unwrap_or_else(|_| panic!("Cmdlet {name} failed to serialize"));
        json.push('\n');
        fs::write(file_path, json)
            .unwrap_or_else(|_| panic!("Cmdlet {name} JSON file failed to write"));
    });
}

fn run_pwsh_command<S: AsRef<str>>(command: S) -> String {
    let stdout_bytes = process::Command::new("pwsh")
        .args(["-NoProfile", "-NoLogo", "-Command", command.as_ref()])
        .output()
        .expect("pwsh must be installed")
        .stdout;
    String::from_utf8(stdout_bytes).expect("pwsh output must be valid UTF8")
}

/// Check if this command has overrides defined. If there is no file for this command, return None.
fn get_overrides(name: &str) -> Option<CommandOverrides> {
    let overrides_path = path::Path::new(env!("CARGO_MANIFEST_DIR"))
        .join("json")
        .join("overrides")
        .join("powershell")
        .join(format!("{}.json", name));

    match fs::File::open(overrides_path) {
        Ok(f) => {
            let overrides = serde_json::from_reader::<_, CommandOverrides>(io::BufReader::new(f))
                .expect(&format!("failed to deserialize {name} overrides"));
            Some(overrides)
        }
        Err(e) => match e.kind() {
            std::io::ErrorKind::NotFound => None,
            _ => panic!("Failed to read file: {}", e),
        },
    }
}

/// Assign the data on the overrides onto the signature spec.
fn apply_overrides(command: &mut Command, overrides: CommandOverrides) -> Result<(), ()> {
    for (i, arg_overrides) in overrides.args.into_iter().enumerate() {
        if !arg_overrides.template.is_empty() {
            let arg = command.args.get_mut(i).ok_or(())?;
            arg.template = arg_overrides.template;
        }
    }

    for option_override in overrides.options {
        let option = command
            .options
            .iter_mut()
            .find(|option| option.name.contains(&option_override.name))
            .ok_or(())?;
        for (i, arg_overrides) in option_override.args.into_iter().enumerate() {
            let arg = option.args.get_mut(i).ok_or(())?;
            arg.template = arg_overrides.template;
        }
    }

    Ok(())
}
